    public void listarProd(){
        DefaultTableModel modelo = (DefaultTableModel) jTable3.getModel();
        
        for (int i = 0; i < Inicio.produtos.size(); i++){
            modelo.addRow(new 
                Object[] {
                    Inicio.produtos.get(i).getCod(),
                    Inicio.produtos.get(i).getProduto(),
                    Inicio.produtos.get(i).getValor(),
                    Inicio.produtos.get(i).getUnidade()
                });
            //echo "sou bem viadinho amo dar meu tobinha hihihi";
        }
    }
    
