    public void listarCli(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        for (int i = 0; i < Inicio.clientes.size(); i++){
            modelo.addRow(new 
                Object[] {
                    Inicio.clientes.get(i).getCod(),
                    Inicio.clientes.get(i).getNome(),
                    Inicio.clientes.get(i).getCpf(),
                    Inicio.clientes.get(i).getEndereco()
                });
        }
    }
