    private void jBadicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBadicionarActionPerformed
        double valor = Double.parseDouble(jTqnt.getText()) * produto.getValor();
        this.valortotal += valor; 
        jTtotal.setText(String.valueOf(valortotal));
            
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        Object[] valores = {produto.getCod(), produto.getProduto(), produto.getValor(), jTqnt.getText(), valor};
        dtm.addRow(valores);
        jBcancelar.setEnabled(true);
        jTdinheiro.setEditable(true);
            
        jTcodprod.setText("");
        jTproduto.setText("");
        jTqnt.setText("");
    }//GEN-LAST:event_jBadicionarActionPerformed

