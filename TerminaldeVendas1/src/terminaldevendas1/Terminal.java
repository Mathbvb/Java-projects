    public void buscarCliente(){
        String texto = jTcodcli.getText();
        setCliente (new Cliente(0,"","",""));
        
        Inicio.clientes.forEach((cliente) ->{
            if (texto == null ? cliente.getCod()+"" == null : texto.equals(cliente.getCod()+"")){
                setCliente(cliente);
            }
        });
        
        if (this.cliente.getCod() != 0){
            jTcliente.setText(this.cliente.getCod() +" - " +this.cliente.getNome());
        } else {
            jTcliente.setText("Não existe");
            setCliente( new Cliente(0,"","",""));
        }
    }
    
    public void buscarProduto(){
        String texto = jTcodprod.getText();
        setProduto (new Produto(0,"",0,""));
        
        Inicio.produtos.forEach((produto) ->{
            if (texto == null ? produto.getCod()+"" == null : texto.equals(produto.getCod()+"")){
                setProduto(produto);
            }
        });
        
        if (this.produto.getCod() != 0){
            jTproduto.setText(this.produto.getCod() +" - " +this.produto.getProduto());
        } else {
            jTproduto.setText("Não existe");
            setCliente( new Cliente(0,"","",""));
        }
    }
    
    public void limpa(){
        setCliente(new Cliente(0, "", "", ""));
        setProduto(new Produto(0, "", 0, ""));
        jTcliente.setText("");
        jTproduto.setText("");
        this.valortotal = 0;
        jTtotal.setText("");
        jTdinheiro.setText("");
        jTtroco.setText("");
        jTcodcli.setText("");
        jTcodprod.setText("");
        jTqnt.setText("");
        jTcodcli.setEnabled(true);
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = jTable1.getRowCount(); i > 0; i--) {
            dtm.removeRow(i-1);
        }
    }
